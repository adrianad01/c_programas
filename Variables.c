#include <stdio.h>

int main(){
	int entero;		// variable entera
	entero=250;
	float decimal;	// variable flotante o decimal
	decimal=250.5;
	char caracter;	// variable de tipo caracter
	caracter='a';
	
	printf("Mi numero entero es: %d\n", entero);
	printf("Mi numero decimal es: %f\n", decimal);
	printf("Mi letra es: %c\n", caracter);
	
	
	

	
	
	return 0;
}
